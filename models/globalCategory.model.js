import mongoose from "mongoose";

const globalCategorySchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
      unique: true,
    },
    image: {
      type: String,
      default: "",
    },
    order: {
      type: Number,
      default: 0,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  { timestamps: true },
);

const GlobalCategory = mongoose.model("GlobalCategory", globalCategorySchema);
export default GlobalCategory;
